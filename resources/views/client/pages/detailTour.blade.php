Khám phá&nbsp;<em><strong>Khu phố Gastown&nbsp;</strong></em>được thành lập từ năm 1867. Khu phố còn giữ được nhiều nét đẹp cổ xưa đan xen với kiến trúc mới, thả bộ trên con đường lát đá với các tiệm cà phê thơ mộng, những hàng quán xinh xắn. Chiêm ngưỡng&nbsp;<em><strong>Tháp Đồng hồ chạy bằng hơi nước&nbsp;</strong></em>độc đáo. Ngắm nhìn&nbsp;<em><strong>Tòa nhà Canada Place&nbsp;</strong></em>lộng lẫy bên bờ sông Burrard Inlet với lớp mái bằng vải như những cánh buồm đón gió. Mua sắm tại những khu bán hàng thời trang cao cấp, sầm uất của thành phố. Nghỉ đêm tại Vancouver.</p>
<p><strong>NGÀY 5: VANCOUVER - LA HABANA (Ăn sáng)</strong><br />
Trả phòng. Ra sân bay làm thủ tục đón chuyến bay dự kiến&nbsp;<em><strong>AC110 (10:05-17:33) và AC1876 (19:35-23:05) từ Vancouver bay đi La Habana, quá cảnh Toronto</strong></em>. Nhận phòng khách sạn 4 sao và nghỉ đêm tại La Habana.</p>
<p><strong>NGÀY 6: LA HABANA (Ăn sáng, trưa, tối)</strong><br />
Chiêm ngưỡng và nghe giới thiệu về những công trình tiêu biểu của thành phố kỳ quan La Habana:&nbsp;<em><strong>Quảng trường Armas&nbsp;</strong></em>được bao quanh bởi những tòa nhà mang phong cách Baroque thanh lịch;&nbsp;<em><strong>Đài tưởng niệm&nbsp;anh hùng Carlos Manuel de Cespedes&nbsp;</strong></em>- được mệnh danh là “cha đẻ của Cuba;&nbsp;<em><strong>New Square - Quảng trường cổ thứ ba của&nbsp;La Habana&nbsp;</strong></em>được xây dựng vào năm 1559;&nbsp;<em><strong>Quảng trường Cách Mạng nằm trên đồi Catalanez; pháo đài cổ El Morro</strong></em>&nbsp;tại đầu kênh đào Entrada trấn giữ lối vào vịnh La Habana. Nghỉ đêm tại La Habana.</p>
<p><strong>NGÀY 7: LA HABANA - VARADERO (Ăn sáng, trưa, tối)</strong><br />
Trả phòng. Khởi hành đi&nbsp;<em><strong>Varadero</strong></em>&nbsp;- một trong những khu nghỉ mát lớn nhất của vùng Caribbean với những bãi cát trắng tuyệt đẹp, cách La Habana khoảng 140km. Quý khách sẽ có một ngày trọn vẹn để&nbsp;<em><strong>tận hưởng dịch vụ tại resort 4 sao Memories Varadero Beach Resort&nbsp;</strong></em>(hoặc tương đương). Tự do thưởng thức các bữa trưa và tối tại các quầy buffets với thực đơn phong phú. Cảm nhận hương vị tinh tế của các loại rượu, cocktail, cà phê miễn phí theo tiêu chuẩn quy định. Tắm nắng và bơi lội trong làn nước biển xanh ngọc hoặc trải nghiệm các dịch vụ spa và các hoạt động giải trí thể thao (chi phí tự túc). Nghỉ đêm tại Varadero.</p>
<p><strong>NGÀY 8: VARADERO - LA HABANA (Ăn sáng, trưa, tối)</strong><br />
Chia tay Varadero và trở lại La Habana. Tham quan&nbsp;<em><strong>bảo tàng rượu Rum, đại lộ Malecon&nbsp;</strong></em>chạy dọc bờ biển,&nbsp;<em><strong>khách sạn Ambos Mundos</strong></em>&nbsp;màu hồng nơi nhà văn Ernest Hemingway đặt bút viết tác phẩm trứ danh «&nbsp;Chuông nguyện hồn ai&nbsp;». Nghỉ đêm tại khách sạn 4 sao tại La Habana.</p>
<p><strong>NGÀY 9: LA HABANA - BẮC KINH (Ăn sáng)</strong><br />
Khởi hành ra sân bay đón chuyến bay&nbsp;<em><strong>CA880 (10:00-17:20) từ La Habana về Bắc Kinh, quá cảnh tại Montreal</strong></em>. Ăn và nghỉ trên máy bay.</p>
